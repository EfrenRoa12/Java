package _1LISTAS;

public class Perro {
	
	//CREAMOS LOS ATRIBUTOS
	String nombre;
	int edad;
	String color;
	int Precio;
	String genero;
	
	//CREAMOS EL CONSTRUCTOR PARA INICIALIZAR EL OBJETO
	public Perro() {
		
	}
	//CREAMOS EL CONSTRUCTOR CON PARAMETROS PARA ESTABLECER LOS ATRIBUTOS DEL OBJETO
	public Perro(String nombre, int edad, String color, int precio, String genero) {
		this.nombre = nombre;
		this.edad = edad;
		this.color = color;
		Precio = precio;
		this.genero = genero;
	}
	
	
	
	
	
	
}
